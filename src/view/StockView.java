    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        Stock stock = new Stock();
        stock.setProduit(produits.get(jComboBox1.getSelectedIndex()));
        stock.setMagasin(magasins.get(jComboBox2.getSelectedIndex()));
        stock.setQte(Double.parseDouble(jTextField4.getText()));
        int res = stockService.create(stock, stocks);
        if (res == 1) {
            JOptionPane.showMessageDialog(null, "nv stock cree", "info", JOptionPane.INFORMATION_MESSAGE);
            stockHelper.add(stock);
        } else if (res == 2) {
            JOptionPane.showMessageDialog(null, "ancien stock mis a jour", "info", JOptionPane.INFORMATION_MESSAGE);
            stockHelper.refresh();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

